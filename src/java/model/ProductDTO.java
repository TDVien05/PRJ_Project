package model;

/**
 *
 * @author tungi
 */
public class ProductDTO {
    private String id;
    private String name;
    private double price;
    private String description;
    private String imageUrl;
    private String categoryId;
    private int stockQuantity;
    private boolean status;
    private int brandId;

    public ProductDTO() {
    }

    public ProductDTO(String id, String name, double price, String description, String imageUrl, String categoryId, int stockQuantity, boolean status, int brandId) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.description = description;
        this.imageUrl = imageUrl;
        this.categoryId = categoryId;
        this.stockQuantity = stockQuantity;
        this.status = status;
        this.brandId = brandId;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public String getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(String categoryId) {
        this.categoryId = categoryId;
    }

    public int getStockQuantity() {
        return stockQuantity;
    }

    public void setStockQuantity(int stockQuantity) {
        this.stockQuantity = stockQuantity;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }

    public int getBrandId() {
        return brandId;
    }

    public void setBrandId(int brandId) {
        this.brandId = brandId;
    }

    @Override
    public String toString() {
        return "ProductDTO{" + "id=" + id + ", name=" + name + ", price=" + price + ", description=" + description + ", imageUrl=" + imageUrl + ", categoryId=" + categoryId + ", stockQuantity=" + stockQuantity + ", status=" + status + ", brandId=" + brandId + '}';
    }

    

}